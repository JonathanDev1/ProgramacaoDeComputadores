# Inicializa a variavel contador com o valor 1
contador = 1

# Enquanto o valor do contador for menor ou igual a 5, o bloco abaixo sera executado
while contador <= 5:
    # Imprime o valor atual do contador
    print(contador)

    # Incrementa o valor do contador em 1
    contador += 1  # mesma coisa que contador = contador + 1
